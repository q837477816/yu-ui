<template>
    <div class="tabs-item" :class="classes" @click="xxx">
        <slot></slot>
    </div>
</template>

<script>
export default {
    name: 'YuTabsItem',
    inject: ['eventBus'],
    props: {
        disabled: {
            typd: Boolean,
            default: false
        },
        name: {
            typs: [String, Number],
            require: true
        }
    },
    data() {
        return {
            active: false
        }
    },
    computed: {
        classes() {
            return {
                active: this.active
            }
        }
    },
    created() {
        this.eventBus.$on('update:selected', (name) => {
            this.active = name === this.name
        })
    },
    methods: {
        xxx() {
            this.eventBus.$emit('update:selected', this.name)
        }
    }
}
</script>

<style lang="scss" scoped>
    .tabs-item {
        // flex-grow: 1;
        flex-shrink: 0;
        padding: 0 1em;
        &.active {
            background: red;
        }
    }
</style>